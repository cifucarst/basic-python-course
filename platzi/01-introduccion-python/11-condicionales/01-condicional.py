# if basico
x = 10

if x > 5:
    print("x es mayor que 5")
# Salida: x es mayor que 5


# if else
x = 3

if x > 5:
    print("x es mayor que 5")
else:
    print("x no es mayor que 5")
# Salida: x no es mayor que 5


# if elif else
x = 7

if x > 10:
    print("x es mayor que 10")
elif x > 5:
    print("x está entre 6 y 10")
else:
    print("x es menor o igual a 5")
# Salida: x está entre 6 y 10



# operador en condiciones
a = 10
b = 5

if a > b:
    print("a es mayor que b")
# Salida: a es mayor que b



# operadores logicos
edad = 18
tiene_permiso = True

if edad >= 18 and tiene_permiso:
    print("Puedes conducir")
else:
    print("No puedes conducir")
# Salida: Puedes conducir
