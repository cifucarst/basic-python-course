# propiedades avanzadas

# tipo dinamico
x = 42
print(type(x))  # <class 'int'>

# enteros grandes
x = 10 ** 100  # Número con 101 dígitos
print(x)

# numeros enteros en diferentes bases
# Binario
binario = 0b1010  # 10 en decimal
print(binario)

# Octal
octal = 0o12  # 10 en decimal
print(octal)

# Hexadecimal
hexadecimal = 0xA  # 10 en decimal
print(hexadecimal)