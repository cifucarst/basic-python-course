# Orden de precedencia detallado

# Paréntesis ()
print((2 + 3) * 4)  # 20
print(2 + (3 * 4))  # 14


# Exponentes **

print(2 ** 3)       # 8
print(2 ** (1 + 1)) # 4


# Multiplicación *, División /, División entera //, y Módulo %

print(10 / 2 * 3)   # 15.0 (de izquierda a derecha)
print(10 // 3 % 2)  # 1

# Suma + y Resta -

print(5 + 3 - 2)  # 6