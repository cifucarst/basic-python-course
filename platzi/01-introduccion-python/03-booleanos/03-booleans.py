# operadores de comparacion

# operador de igualdad: ==
print(5 == 5)   # True
print(5 == 3)   # False

# desigualdad
print(5 != 3)   # True
print(5 != 5)   # False

# mayor que >, menor que <
print(10 > 5)   # True
print(3 < 2)    # False

# Mayor/menor o igual: >=, <=
print(5 >= 5)   # True
print(4 <= 3)   # False
