# Tuplas básicas
# Tupla con elementos
mi_tupla = (1, 2, 3)
print(mi_tupla)  # (1, 2, 3)

# Tupla vacía
tupla_vacia = ()
print(tupla_vacia)  # ()

# Tupla con un solo elemento (nota la coma)
una_tupla = (42,)
print(una_tupla)  # (42,)


# tuplas sin parentesis
mi_tupla2 = 1, 2, 3
print(mi_tupla2)  # (1, 2, 3)


lista = [1, 2, 3]
mi_tupla = tuple(lista)
print(mi_tupla)  # (1, 2, 3)